DROP TABLE HD_EVENT_LOG;
CREATE TABLE HD_EVENT_LOG (
    COMMON_NAME VARCHAR2(1024 CHAR), 
    DESCRIPTION VARCHAR2(1024 CHAR), 
    FUNCTION_NAME VARCHAR2(1024 CHAR), 
    SOURCE_IP_ADDRESS VARCHAR2(1024 CHAR), 
    WAIT_TIME VARCHAR2(1024 CHAR), 
    HH_USER VARCHAR2(1024 CHAR), 
    LAST_PASSWORD VARCHAR2(1024 CHAR), 
    NEW_PAWSSWORD VARCHAR2(1024 CHAR), 
    CATEGORY VARCHAR2(1024 CHAR), 
    LAST_UPDATE_DATE DATE, 
    LAST_MODIFICATION_BY VARCHAR2(1024 CHAR)
);
 
